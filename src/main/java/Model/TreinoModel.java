package Model;

/**
 *
 * @author asg75
 */
public class TreinoModel {
    protected String titulo;
    protected String descricao;

    public TreinoModel(String titulo, String descricao) {
        this.titulo = titulo;
        this.descricao = descricao;
    }

    public String getTitulo() {
        return titulo;
    }

    public String getDescricao() {
        return descricao;
    }
}
